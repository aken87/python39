class Person:
#                p1______________________________________________________________________________________          
#                 |                                                                                 #    ↑ 
#                 |        2020__________________________________________________________________________|
#                 ↓          ↓                                                                      #    ↑  
    def get_age(self, current_year):                                                                #    |  
#                 ↓__________|_______p1      1979_______________________________                    #    |                                                  #    |  
#                            ↓        ↓        ↓                                ↑                   #    |  
        print(f"Age: {current_year - self.year_of_birth}")#                     |                   #    |  
#                                                                               |                   #    |  
#                                                                               |                   #    |  
#                   p1__________________________________________________________|_____________________   |  
#                    ↓                                                          |                   # ↑  |  
    def print_info(self):#                                                      |                   # |  |  
#                    |                                                          |                   # |  |  
#                    ↓__________________________________________________        |                   # |  |  
#                       ↓                      ↓                        ↓       ↑                   # |  |  
#                      p1   Elon              p1   Musk                p1     1979                  # |  |  
#                       ↓     ↓                ↓     ↓                  ↓       ↓                   # |  |  
        print(f"Name: {self.name}, Surname: {self.surname}, Birth in: {self.year_of_birth}")        # |  |  
#                             ↑                      ↑                          ↑                   # |  |         
p1 = Person()#                |                      |                          |                   # |  |  
